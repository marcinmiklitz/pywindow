    np.testing.assert_equal(
        molsys_rebuild.system["elements"],
        output_molsys.system["elements"],
    )
    np.testing.assert_almost_equal(
        molsys_rebuild.system["coordinates"],
        output_molsys.system["coordinates"],
    )
    tmp_path.unlink()


